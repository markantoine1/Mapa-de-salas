    public void readCbbCursos(){
        DefaultComboBoxModel modelo = (DefaultComboBoxModel) cbbCurso.getModel();
        AlocacoesDAO adao = new AlocacoesDAO();
        for (Alocacoes a  : adao.readCourses()) {
            modelo.addElement(
                a.getCurso()
            );
    
        }
    }
    public void readCbbDocentes(){
        DefaultComboBoxModel modelo = (DefaultComboBoxModel) cbbDocente.getModel();
        AlocacoesDAO adao = new AlocacoesDAO();
        for (Alocacoes a  : adao.readTeachers()) {
            modelo.addElement(
                a.getDocente()
            );
    
        }
    }
    
    
