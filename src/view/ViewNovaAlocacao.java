    private void btnAlocarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlocarActionPerformed
        // TODO add your handling code here:
        Alocacoes a = new Alocacoes();
        AlocacoesDAO adao = new AlocacoesDAO();
       
        if (btnManha.isSelected()) {
            a.setTurnoManha(true);
        }
        if (btnTarde.isSelected()) {
            a.setTurnoTarde(true);
        }
        if (btnNoite.isSelected()) {
            a.setTurnoNoite(true);
        }
        a.setCurso(cbbCurso.getSelectedItem().toString());
        a.setDocente(cbbDocente.getSelectedItem().toString());
        a.setSala(cbbSala.getSelectedItem().toString());
        a.setData(jdtData.getDate());
        a.definirTurno();
        int r = new Random().nextInt(1000, 9999);
        a.setId(r);
        adao.create(a);
        
        btnManha.setSelected(false);
        btnTarde.setSelected(false);
        btnNoite.setSelected(false);
        jdtData.cleanup();
        
    }//GEN-LAST:event_btnAlocarActionPerformed

